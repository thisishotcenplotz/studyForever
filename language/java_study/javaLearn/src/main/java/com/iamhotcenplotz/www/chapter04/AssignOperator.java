package com.iamhotcenplotz.www.chapter04;

public class AssignOperator {
    public static void main(String[] args) {
        int n1 = 10;
        n1 +=4;
        System.out.println(n1);

        n1 /= 3;
        System.out.println(n1);

        n1 *= 3;
        System.out.println(n1);
        n1 %= 3;
        System.out.println(n1);
        n1++;
        System.out.println(n1);
    }
}
